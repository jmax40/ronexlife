// get start date and end date input elements
const startDateInput = document.getElementById('startdate');
const endDateInput = document.getElementById('enddate');

// calculate the remaining time between start date and end date
function calculateTimeRemaining() {
  const startDate = new Date(startDateInput.value).getTime();
  const endDate = new Date(endDateInput.value).getTime();
  const timeRemaining = endDate - startDate;

  const days = Math.floor(timeRemaining / (1000 * 60 * 60 * 24));
  const hours = Math.floor((timeRemaining % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
  const minutes = Math.floor((timeRemaining % (1000 * 60 * 60)) / (1000 * 60));
  const seconds = Math.floor((timeRemaining % (1000 * 60)) / 1000);

  return {
    days: days < 10 ? "0" + days : days,
    hours: hours < 10 ? "0" + hours : hours,
   
  }}